package net.konic.corejava.RelationalOperators;

public class related {
	public static void main(String[] args) {
		int a=40;
		int b=60;
		System.out.println("a==b:"+(a==b));
		System.out.println("a!=:"+(a!=b));
		System.out.println("a>b:"+(a>b));
		System.out.println("a<b:"+(a<b));
		System.out.println("a>=b:"+(a>=b));
		System.out.println("a<=b;"+(a<=b));
		
	}

}
